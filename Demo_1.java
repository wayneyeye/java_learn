/*
Demo for different types in java
*/
public class Demo_1{
	public static void main(String[] args){
		System.out.println(10);
		System.out.println(0B10011101);
		System.out.println(0XE); //hex
		System.out.println(5.0);//float
		System.out.println(true);//boolean
		System.out.println('a');//char, one and only one letter is allowed
		System.out.println("Hi, this is a string");//char, only one letter is allowed
		
	}
}